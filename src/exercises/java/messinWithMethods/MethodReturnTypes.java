package exercises.java.messinWithMethods;

public class MethodReturnTypes {
    //This method returns an int, as designated by the int before the method name.  The return statement returns the
    //value of val1 + val2 in this method.
    public int sum (int val1, int val2) {
        return val1 + val2;
    }

    //You can return any primitive Java Type in a Java method.  You can return a String too, as in this example:
    public String combine (String val3, String val4) {
        return val3 + val4;
    }

}
