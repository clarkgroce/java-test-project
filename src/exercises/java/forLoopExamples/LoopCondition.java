package exercises.java.forLoopExamples;

public class LoopCondition {

    public static void main(String[] args) {

        //The Loop condition is the middle section and should evaluate to true
        //or false.  If true it will run through the loop again, and the post iteration operation will execute
        // (i++) and if false the loop will stop execution.
        for (int i = 0; i < 10; i ++) {

            System.out.println(" i = " + i);

        }

    }

}
