package exercises.java.stringExamples;

public class StringCharLocation {

    public static void main(String[] args) {

        String test = "Yo where my pizza at!";
        //the String class method charAt() will return the char at the index you pass as an argument in the method.
        System.out.println(test.charAt(0));
        System.out.println(test.charAt(6));

    }

}
